namespace Scand\RhenusWMS\Messages\Segments\DataTypes;

abstract class DataType implements DataTypeInterface
{
    protected $type;
    protected $title;
    protected $value;
    protected $length;
    protected $format;

    /**
     * Validates attribute value based on format
     */
    abstract public function validate();

    /**
     * Returns true if ERP system can change this attribute
     */
    public function canUpdate($authority)
    {
        return $authority == DataTypeInterface::AUTHORITY_CUSTOMER_CAN_CNAGE;
    }

    /**
     * Validate by regex in format property
     */
    public function validateByFormat()
    {
        if (isset($this->format)) {
            return preg_match($this->format, $this->value);
        }
        return true;
    }

    /**
     * @param mixed $format
     */
    public function setFormat($format)
    {
        $this->format = $format;
    }

    /**
     * @return mixed
     */
    public function getFormat()
    {
        return $this->format;
    }

    /**
     * @param mixed $length
     */
    public function setLength($length)
    {
        $this->length = $length;
    }

    /**
     * @return mixed
     */
    public function getLength()
    {
        return $this->length;
    }

    /**
     * @param mixed $title
     */
    public function setTitle($title)
    {
        $this->title = $title;
    }

    /**
     * @return mixed
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param mixed $type
     */
    public function setType($type)
    {
        $this->type = $type;
    }

    /**
     * @return mixed
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * @param mixed $value
     */
    public function setValue($value)
    {
        $this->value = $value;
    }

    /**
     * @return mixed
     */
    public function getValue()
    {
        return $this->value;
    }
}
